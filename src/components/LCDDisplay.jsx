export const LCDDisplay = ({ line1 = '', line2 = '', width, height, opacity = 1 }) => {
    const paddingX = width * 0.05; // 3% horizontal padding
    const paddingY = height * 0.11; // 8% vertical padding
    const availableWidth = width - (paddingX * 2);
    const availableHeight = height - (paddingY * 2);

    // Calculate pixel size based on available space
    // 8 rows per char * 2 lines + 1 pixel gap between lines = 17 rows total
    const pixelSizeByHeight = availableHeight / 17;
    // 16 characters * (5 pixels wide + 1 spacing) - 1 (no spacing after last char) = 95 pixels wide
    const pixelSizeByWidth = availableWidth / 95;

    // Use the smaller of the two to ensure everything fits
    const pixelSize = Math.min(pixelSizeByHeight, pixelSizeByWidth);

    const renderChar = (char, charIndex, lineIndex) => {
        const bitmap = FONT[char.toUpperCase()] || FONT[' '];
        const charX = paddingX + (charIndex * 6 * pixelSize); // 5 pixels wide + 1 pixel spacing
        const charY = paddingY + (lineIndex * 9 * pixelSize); // 8 pixels + 1 pixel gap

        const pixels = [];
        bitmap.forEach((row, rowIndex) => {
            row.forEach((pixel, colIndex) => {
                pixels.push(
                    <rect
                        key={`${charIndex}-${rowIndex}-${colIndex}`}
                        x={charX + colIndex * pixelSize}
                        y={charY + rowIndex * pixelSize}
                        width={pixelSize * 0.9}
                        height={pixelSize * 0.9}
                        fill={pixel === 1 ? "white" : "rgba(83, 170, 255, 0.3)"}
                    />
                );
            });
        });
        return pixels;
    };

    // Pad lines to always be 16 characters
    const line1Padded = (line1 + ' '.repeat(16)).substring(0, 16);
    const line2Padded = (line2 + ' '.repeat(16)).substring(0, 16);

    return (
        <svg
            width={width}
            height={height}
            style={{ opacity }}
        >
            {/* Render line 1 (always 16 characters) */}
            {line1Padded.split('').map((char, index) => (
                <g key={`l1-${index}`}>
                    {renderChar(char, index, 0)}
                </g>
            ))}

            {/* Render line 2 (always 16 characters) */}
            {line2Padded.split('').map((char, index) => (
                <g key={`l2-${index}`}>
                    {renderChar(char, index, 1)}
                </g>
            ))}
        </svg>
    );
};
